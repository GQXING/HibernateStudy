package com.gqx.extends4;
/**
 * 动物类
 * @author 郭庆兴
 *
 */
public abstract class Animal {
	private String id;
	private String name;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
